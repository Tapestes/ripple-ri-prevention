package com.mitre.holdshort;

import android.graphics.Bitmap;
import android.graphics.Canvas;
import android.graphics.Color;
import android.graphics.Paint;
import android.graphics.PorterDuffXfermode;
import android.graphics.Rect;
import android.graphics.RectF;
import android.graphics.Bitmap.Config;
import android.graphics.PorterDuff.Mode;
import android.graphics.drawable.BitmapDrawable;

public class ImageHelper {
    public BitmapDrawable getRoundedCornerBitmap(Bitmap bitmap, int pixels) {
        Bitmap output = Bitmap.createBitmap(bitmap.getWidth(), bitmap
                .getHeight(), Config.ARGB_8888);
        Canvas canvas = new Canvas(output);
       

        final Paint paint = new Paint();
        Paint paintStroke = new Paint();

        paintStroke.setStrokeWidth(3);
        paintStroke.setStyle(Paint.Style.STROKE);
        paintStroke.setColor(Color.WHITE);
        paintStroke.setAntiAlias(true);
        
        final Rect rect = new Rect(0, 0, bitmap.getWidth(), bitmap.getHeight());
        final RectF rectF = new RectF(rect);
        final float roundPx = pixels;

        paint.setAntiAlias(true);
        canvas.drawARGB(0, 0, 0, 0);
        canvas.drawRoundRect(rectF, roundPx, roundPx, paint);

        paint.setXfermode(new PorterDuffXfermode(Mode.SRC_IN));
        canvas.drawBitmap(bitmap, rect, rect, paint);
        
        final Rect stroke_rect = new Rect(1, 1, bitmap.getWidth()-1, bitmap.getHeight()-1);
        final RectF stroke_rectF = new RectF(stroke_rect);
        canvas.drawRoundRect(stroke_rectF, roundPx, roundPx, paintStroke);

        return new BitmapDrawable(output);
    }
}
